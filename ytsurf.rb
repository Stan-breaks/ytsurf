  desc "YouTube in your terminal. Clean and distraction-free"
  homepage ""
  url "https://github.com/Stan-breaks/ytsurf/archive/refs/tags/v3.1.7.zip"
  sha256 "6a12afed36ea7a62036291e603350b1301ab7cce64fbe723e8ba20d5232ea6f9"
  version "3.1.7"
  license "GPL-3.0"
  
  depends_on "bash"
  depends_on "yt-dlp"
  depends_on "jq"
  depends_on "curl"
  depends_on "mpv"
  depends_on "perl"
  depends_on "fzf"
