class Ytsurf < Formula
  desc "YouTube in your terminal. Clean and distraction-free"
  homepage ""
  url "https://github.com/Stan-breaks/ytsurf/archive/refs/tags/v3.1.7.zip"
  sha256 "9c87c3677f5074dabff4acc41e51c910b9c3ee05e18e6e43dac421d4ca24c0b3"
  version "3.1.8"
  license "GPL-3.0"
  
  depends_on "bash"
  depends_on "yt-dlp"
  depends_on "jq"
  depends_on "curl"
  depends_on "mpv"
  depends_on "perl"
  depends_on "fzf"
  depends_on "chafa"
  depends_on "ffmpeg"

  def install
